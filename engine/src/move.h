#pragma once

#include "card.h"

#include <vector>
#include <cstdint>

namespace durakk {

// Тип действия, которое бот предлагает выполнить на текущем шаге игры.
enum class Action : uint8_t {
    Attack,    // походить картой (начать или продолжить атаку)
    Defend,    // побить конкретную атакующую карту своей
    Transfer,  // перевод: положить карту того же ранга рядом, переняв атаку
    Toss,      // подкидывание: добавить карту уже присутствующего на столе ранга
    Take,      // взять все карты со стола в руку (не отбился)
    Done,      // закончить кон: «бито» (защищающийся отбился и больше нет подкидываний)
    Pass,      // пропустить дальнейшее подкидывание (атакующий не подкидывает)
};

// Описание пары «атакующая карта → защита» на столе.
struct TablePair {
    Card attack;                 // карта атаки/подкидывания
    Card defense;                // карта, которой побили (если есть)
    bool defended = false;       // true, если защита есть
};

// Предлагаемый ботом ход.
struct Move {
    Action action;

    // Для Attack/Defend/Transfer/Toss — карта, которой играем.
    Card card{};

    // Для Defend — какую именно атакующую карту на столе бьём.
    // Если на столе несколько непобитых одинаковых карт, бьём «первую непобитую».
    Card target{};
    bool hasTarget = false;

    // Текстовое пояснение для UI (почему выбран этот ход). Помогает на этапе
    // эвристики и отладки; позже будет использоваться для «подсказок игроку».
    std::string reason;
};

} // namespace durakk
