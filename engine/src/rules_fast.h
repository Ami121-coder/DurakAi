#pragma once

// Быстрые масочные генераторы ходов и default policy для rollout-фазы ISMCTS.
//
// В отличие от rules.cpp (который работает с GameState/vector<Card> и предназначен
// для UI/валидации), этот модуль оперирует MatchState + CardMask напрямую и
// минимизирует аллокации — это горячий путь, выполняющийся тысячи раз в секунду
// в плейаутах.
//
// Соглашение: функции заполняют переданный буфер и возвращают число записанных
// элементов. Буфер имеет фиксированный размер (максимально возможное число
// легальных ходов в позиции), чтобы избежать аллокаций в куче.

#include "match.h"
#include "move.h"

#include <array>

namespace durakk {

// Максимально возможное число «карточных» ходов в одной позиции:
//   защита — до 36 карт × до 6 целей ≈ мало; на практике не больше ~36.
// Берём с запасом.
constexpr int kMaxMoves = 64;

using MoveBuffer = std::array<Move, kMaxMoves>;

// Записать в buf все легальные ходы для атакующей стороны (Attack/Toss/Done).
// Возвращает количество записанных. doneOut (если не null) устанавливается,
// когда легален ход Done (все побито и можно завершить кон).
int genAttackMoves(const MatchState& s, MoveBuffer& buf, bool* doneOut = nullptr);

// Записать в buf все легальные ходы защищающегося: Defend, Transfer, Take.
int genDefenseMoves(const MatchState& s, MoveBuffer& buf);

// Универсальный генератор по текущей фазе. Возвращает кол-во ходов.
int genLegalMoves(const MatchState& s, MoveBuffer& buf);

// ------------------------- Default policy (rollout) -------------------------
//
// Лёгкая эвристика выбора хода для playout. Не оптимальна, но даёт более
// «осмысленные» симуляции, чем чистый random — что снижает дисперсию оценки ISMCTS.
// Возвращает ход, выбранный из buf[0..n), или false, если n==0.
//
// Предпочтения (мягкие):
//   • при защите — побить младшей достаточной картой, избегая козыря;
//   • при атаке — играть младшей некозырной;
//   • иногда (с заданной вероятностью) — взять/перевести, если это выглядит выгодно.
bool defaultPolicy(const MatchState& s, const Move* buf, int n, Move& out);

} // namespace durakk
