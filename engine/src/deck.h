#pragma once

#include "card.h"

#include <vector>
#include <cstddef>

namespace durakk {

// Размер колоды. В классическом дураке онлайн — 36 карт (6..Туз).
// 24 (9..Туз) и 52 — поддерживаются правилами, но здесь по умолчанию 36.
enum class DeckSize : uint8_t {
    Cards24 = 24,
    Cards36 = 36,
    Cards52 = 52,
};

// Колода: набор ещё не розданных карт + открытый козырь.
// Реальный порядок карт движку-советнику неизвестен (мы не знаем, что выпадет),
// поэтому deck хранит только количество оставшихся карт; сами карты — приватные
// данные стола. Козырь известен точно.
struct Deck {
    Suit trump = Suit::Spades;
    int remaining = 0;        // сколько карт осталось вытянуть (включая нижнюю козырь)
    DeckSize size = DeckSize::Cards36;
};

// Минимальный ранг в колоде заданного размера (для построения полной колоды
// и валидации пользовательского ввода).
Rank minRank(DeckSize size);

// Полная колода заданного размера (без перемешивания).
std::vector<Card> fullDeck(DeckSize size);

// Сколько карт раздаётся на руки в начале (константа дурака).
constexpr std::size_t kHandSize = 6;

} // namespace durakk
